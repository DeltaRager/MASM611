DATA SEGMENT
	MSG DB "LENGTH OF STRING IS: $"
	STR1 DB "DUNDE$"
	LEN EQU $-STR1
DATA ENDS

 

CODE SEGMENT
	ASSUME DS:DATA,CS:CODE
START:

 

	MOV AX,DATA
	MOV DS,AX
	LEA SI, STR1
	MOV cx, 0000H
	mov bl, "$"
L1:
	CMP [SI], bl
	JE L2
	INC SI
	INC cl
	JMP L1
L2:
	
	LEA DX, MSG
	MOV AH, 09H
	INT 21H
	MOV DL, cl
	ADD DL, 30H
	MOV AH, 02H
	INT 21H
	MOV AH, 4CH
	INT 21H
	HLT
CODE ENDS
END START
